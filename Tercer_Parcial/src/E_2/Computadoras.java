package E_2;

public class Computadoras {
    String marca;
    int cantidadMemoria;
    String característicasProcesador;
    String sistemaOperativo;
    double precio;

    public Computadoras(String marca, int cantidadMemoria, String característicasProcesador, String sistemaOperativo, double precio) {
        this.marca = marca;
        this.cantidadMemoria = cantidadMemoria;
        this.característicasProcesador = característicasProcesador;
        this.sistemaOperativo = sistemaOperativo;
        this.precio = precio;
    }
}
